--INDEX TRÊN THUỘC TÍNH "MÃ KHÁCH HÀNG" CỦA BẢNG "THÔNG TIN ĐƠN HÀNG"
USE TIKI_INDEX
--TÌM ĐƠN HÀNG THEO TÊN MÃ KH
CREATE PROC USP_TIMDH_INDEX @makh INT
AS
	BEGIN
		SELECT * FROM dbo.THONGTINDONHANG d
		WHERE d.MAKH = @makh
	END
GO

USE TIKI
--TÌM ĐƠN HÀNG THEO TÊN MÃ KH
CREATE PROC USP_TIMDH @makh INT
AS
	BEGIN
		SELECT * FROM dbo.THONGTINDONHANG d
		WHERE d.MAKH = @makh
	END
GO

USE TIKI_INDEX

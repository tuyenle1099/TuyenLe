--INDEX TRÊN THUỘC TÍNH "TÊN SẢN PHẨM" CỦA BẢNG "SẢN PHẨM"
USE TIKI_INDEX
--TÌM SẢN PHẨM THEO TÊN SẢN PHẨM
CREATE PROC USP_TIMSP_INDEX @tenSP NVARCHAR(100)
AS
	BEGIN
		SELECT * FROM SANPHAM WHERE TENSP LIKE @tenSP + '%'
	END
GO

USE TIKI
--TÌM SẢN PHẨM THEO TÊN SẢN PHẨM
CREATE PROC USP_TIMSP @tenSP NVARCHAR(100)
AS
	BEGIN
		SELECT * FROM SANPHAM WHERE TENSP LIKE @tenSP + '%'
	END
GO

